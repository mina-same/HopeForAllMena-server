async function addArabicContent() {
  try {
    console.log('Starting to add Arabic content to existing blogs...');
    
    for (const [slug, arabicData] of Object.entries(arabicBlogContent)) {
      console.log(`Processing blog with slug: ${slug}`);
      
      const blog = await Blog.findOne({ slug });
      if (blog) {
        blog.titleAr = arabicData.titleAr;
        blog.excerptAr = arabicData.excerptAr;
        blog.contentAr = arabicData.contentAr;
        blog.tagsAr = arabicData.tagsAr;
        
        await blog.save();
        console.log(`✅ Updated blog: ${blog.title}`);
      } else {
        console.log(`❌ Blog not found with slug: ${slug}`);
      }
    }
    
    console.log('✅ Finished adding Arabic content to blogs');
    process.exit(0);
  } catch (error) {
    console.error('❌ Error adding Arabic content:', error);
    process.exit(1);
  }
}

// Run the script
addArabicContent();
